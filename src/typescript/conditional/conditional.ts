var booleanCondition = true;
  
if (booleanCondition) {
    console.log("Completed");
}
  
// NOT
if (!booleanCondition) {
    console.log("Not Completed");
}
  
// supports standard operators
var anotherConditon = true;
if (booleanCondition && anotherConditon) {
    // both true
}
  
if (booleanCondition || anotherConditon) {
    // one true
}
  